using System;

namespace MyLib
{
	static public class Calculator
	{
		static public int Add(int a, int b)
		{
			return a + b;
		}
		static public int Sub(int a, int b)
		{
			return a - b;
		}
		static public int Multiply(int a, int b)
		{
			return a * b;
		}
		static public int Divide(int a, int b)
		{
			return a / b;
		}
	}
}
